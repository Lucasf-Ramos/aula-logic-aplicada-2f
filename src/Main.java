public class Main {
    public static void main(String[] args) {

       int cont = 0;
       do{
           cont++;
           System.out.println(cont);
       }while(cont < 10);

       int contador = 0;
       while(contador < 10){
           contador++;
           System.out.println(contador);
       }

       for(int x = 0; x<10; x++){
            System.out.println(x);
       }
    }
}